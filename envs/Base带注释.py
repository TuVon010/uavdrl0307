import numpy as np

# rl-gym
class Base:
    def __init__(self):
        # =====================================================================
        # 1. 场景设置 (Scenario Settings)
        # =====================================================================
        self.n_uavs = 3          # 场景中无人机 (UAV) 的数量
        self.n_users = 10        # 场景中地面移动用户 (MU) 的数量
        self.field_X = [0, 1000] # 地图的 X 轴范围 (米)
        self.field_Y = [0, 1000] # 地图的 Y 轴范围 (米)
        self.h = 50              # 无人机的固定飞行高度 (米)。注意：高度直接影响视距(LoS)概率和信号覆盖范围。
        self.time_step = 1.0     # 仿真中每一个离散时隙 (Step) 的真实物理时间 (秒)
        self.coverage_radius = 100.0 # 无人机的有效服务覆盖半径 (米)。(注：计算 coverage_reward 时判断用户是否在圈内的依据)

        # 初始化布局参数
        self.uav_init_radius = 350.0    # 初始状态下，3架无人机均匀分布在一个以地图中心为圆心、该值为半径的圆上 (避免出生就碰撞)
        self.user_cluster_radius = 80.0 # 初始状态下，用户聚集在地图中心，这是高斯分布的标准差 (越小聚集越密集，代表突发事件/热点区域)

        # =====================================================================
        # 2. 归一化参数 (Normalization Parameters) -> RL 收敛的核心命脉
        # 神经网络喜欢 [-1, 1] 的数据，物理量必须除以这些参数进行缩放。
        # =====================================================================
        self.norm_pos = 1000.0          # 坐标归一化基准 (通常取地图最大长度)
        self.norm_data = 5e6            # 任务数据量归一化基准 (5 Mbits)
        self.norm_cycle = 1000.0        # CPU 周期数归一化基准 (1000 cycles/bit)
        self.norm_freq = 10e9           # CPU 频率归一化基准 (10 GHz)
        
        self.norm_energy_uav = 600.0    # 无人机单步最大能耗参考值 (焦耳)，用于计算 energy_pen
        self.norm_energy_user = 5.0     # 用户单步最大能耗参考值 (焦耳)，用于计算 user_cost 惩罚

        # =====================================================================
        # 3. 通信与信道参数 (Urban LoS 概率视距模型)
        # 这里决定了“近处信号极好，远处信号极差(被遮挡)”的悬崖效应。
        # =====================================================================
        self.f_c = 1e9            # 载波频率 1 GHz (计算多普勒频移或某些特定信道模型时可能会用，当前代码主要用 beta0)
        self.c = 3e8              # 光速 (3x10^8 m/s)
        self.alpha_los = 2.0      # 视距 (LoS) 路径损耗指数 (类似自由空间，衰减慢，信号好)
        self.alpha_nlos = 3.8     # 非视距 (NLoS) 路径损耗指数 (被楼房严重遮挡，衰减极快，信号极差)
        
        # a 和 b 是密集城市(Dense Urban)环境下的概率视距模型参数，决定了概率曲线的陡峭程度
        self.a = 9.61             # 环境因子 a
        self.b = 0.16             # 环境因子 b
        
        self.beta0 = 10**(-50/10) # 距离为 1 米时的参考信道增益 (设定为 -50 dB)。拉大这个值可以让正上方的速率非常可观。
        self.sigma2 = 1e-13       # 接收端的高斯白噪声功率 (瓦)
        self.B_total = 20e6       # 无人机的总上行带宽 20 MHz (会均分给所有关联的用户)

        # =====================================================================
        # 4. 智能体物理属性 (Agent Physical Attributes)
        # =====================================================================
        # --- UAV (无人机边缘服务器) ---
        self.uav_v_max = 15.0     # 无人机的最大水平飞行速度 (15 m/s)
        self.C_uav = 20e9         # 无人机携带的边缘服务器的总算力 (20 GHz = 20 * 10^9 cycles/s)。[核心：这是可分配算力的天花板！]
        self.xi_m = 8.2e-10       # 无人机 CPU 的有效开关电容 (用于计算无人机的计算能耗: E = xi_m * cycles)
        
        # 无人机飞行能耗参数 (基于旋翼无人机的严格空气动力学模型)
        self.P0 = 79.86           # 叶片型阻力功率 (悬停时也需要消耗的固定功率之一)
        self.Pi = 88.63           # 诱导功率 (克服重力所需的功率)
        self.U_tip = 120          # 旋翼叶尖速度
        self.v0 = 4.03            # 平均旋翼诱导速度
        self.d0 = 0.6             # 机身阻力比
        self.rho = 1.225          # 空气密度 (kg/m^3)
        self.s = 0.05             # 旋翼实度
        self.A_rotor = 0.5        # 旋翼圆盘面积 (m^2)

        # --- User (地面移动设备/手机) ---
        self.p_tx_max = 0.2       # 用户的最大上行传输功率 (0.2 瓦 = 200 mW)
        self.C_local = 2e9        # 用户本地 CPU 的总算力 (2 GHz = 2 * 10^9 cycles/s)。[核心：决定了纯本地计算能有多快]
        self.k_local = 1e-28      # 用户设备的能量效率系数 (决定了手机发热/掉电的速度)
        
        # =====================================================================
        # 5. Gauss-Markov 移动模型参数 (User Mobility)
        # 决定了用户如何像真人在街道上一样走走停停、有方向感地漫步。
        # =====================================================================
        self.mobility_slot = 1.0                # 用户状态更新的时间间隔 (s)
        self.user_mean_velocity = 0.5           # 用户的平均步行速度 (0.5 m/s)
        self.user_mean_direction = 0.1          # 用户的平均行进方向弧度
        self.user_memory_level_velocity = 0.6   # 速度记忆水平 (0~1之间，越大速度越平滑，越小越随机突变)
        self.user_memory_level_direction = 0.8  # 方向记忆水平 (0~1之间，越大越不容易频繁掉头)
        self.user_Gauss_variance_velocity = 0.5 # 速度波动的高斯方差
        self.user_Gauss_variance_direction = 0.5# 方向波动的高斯方差

        # =====================================================================
        # 6. 任务生成 (Task Generation)
        # 医疗物联网任务的特征：数据量适中，但对计算和时延要求高。
        # =====================================================================
        self.task_size_min = 1e6  # 单个任务最小数据量 (1 Mbits)
        self.task_size_max = 2e6  # 单个任务最大数据量 (2 Mbits)
        self.cycles_min = 500     # 处理 1 bit 数据所需的最小 CPU 周期数
        self.cycles_max = 1000    # 处理 1 bit 数据所需的最大 CPU 周期数 (代表任务的计算复杂度)
        self.latency_max = 3.0    # [极其重要] 任务的硬性截止时间 (Deadline) = 3.0 秒。超过就会触发高额违约惩罚！

        # =====================================================================
        # 7. 成本函数权重 (Objective Function Weights) 
        # 对应论文系统模型 Eq.28 中的权重配置。
        # =====================================================================
        self.mu_L = 1.0           # 系统目标函数中，时延总成本的权重
        self.mu_E = 0.5           # 系统目标函数中，能耗总成本的权重 (由于能耗数值往往较大，通常给一个小权重平衡量级)

        self.omega_H = 1.2        # 高优先级用户 (如急救数据) 的时延和能耗放大系数
        self.omega_L = 1.0        # 低优先级用户 (如日常体征上报) 的常规系数

        # =====================================================================
        # 8. RL 奖励函数子项权重 (Reward Shaping Weights)
        # 决定了智能体在训练中“最怕什么、最想要什么”。
        # =====================================================================
        self.w_penalty = 2.0        # 全局超时惩罚权重。用户一旦超时(>3.0s)，系统奖励就猛扣。
        self.w_overboundary = 0.5   # 无人机越界惩罚权重。防止无人机飞出地图。
        self.w_collision = 1.0      # 无人机碰撞惩罚权重。靠得太近像弹簧一样扣分弹开。

        self.w1_user = 0.4          # 用户奖励构成中，关心“集体大锅饭 (System Reward)”的权重
        self.w2_user = 0.6          # 用户奖励构成中，关心“个人手机电量和时延 (Cost)”的权重 (手机很自私，不想掉电)
        
        self.w_sys_uav = 0.3        # 无人机奖励构成中，关心“集体大锅饭”的权重 (逼迫它提供好服务)
        self.w_ind_uav = 0.7        # 无人机奖励构成中，关心“个人飞行KPI”的权重 (逼迫它飞到好位置)

        self.w_proximity = 0.5      # [个人KPI] 无人机飞向全场人群几何中心的牵引力权重 (宏观引导)
        self.w_coverage = 0.5       # [个人KPI] 无人机覆盖(100m内)用户数量的奖励权重 (微观散开)

        # =====================================================================
        # 9. 安全约束 (Safety Constraints)
        # =====================================================================
        self.uav_safe_dist = 20.0   # 两架无人机之间的最小安全距离。小于 20 米就开始扣碰撞分。
        self.boundary_warn = 50.0   # 距离地图边界的危险缓冲区。距离边界小于 50 米就开始扣越界分。